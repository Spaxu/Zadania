package L3Z2;

public class Movie {
    final String title;
    final String directorName;
    final String directorSurname;
    final int durationInMinutes;
    final int prodYear;


    public Movie(String title, String directorName, String directorSurame, int durationInMinutes, int prodYear) {
        this.title = title;
        this.directorName = directorName;
        this.directorSurname = directorSurame;
        this.durationInMinutes = durationInMinutes;
        this.prodYear = prodYear;
    }
}
